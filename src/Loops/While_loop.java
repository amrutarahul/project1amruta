package Loops;

public class While_loop {

	public static void main(String[] args) {
		// i want to print 6 table
		
		int k=6;
		while(k<=60)
		{
			System.out.println(k);
			k=k+6;
		}
		

	//i want to rahul print 10 times
	   
		String name="Rahul";
	    
	    int r=1;
	    while(r<=10)
	    {
	    	System.out.println(name);
	    	r++;
	    }
	}
}
